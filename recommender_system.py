"""we are grouping the data by each item and then looking at two things: the average rating for each item, and how many times each item was rated. This helps you see not only which items are rated highly, but also how many people rated them, so you know how reliable those ratings are."""

item_stats = df.groupby('item_id')['rating'].agg(['mean', 'count'])

item_stats['count_norm'] = item_stats['count'] / item_stats['count'].max()
item_stats['popularity_score'] = (
    0.7 * item_stats['mean'] +
    0.3 * item_stats['count_norm']
)

"""This function recommends items to a user depending on how much information you have about them.

If the user is completely new and hasn’t reviewed anything, it simply recommends the most popular items overall.

If the user has written some reviews, it does a mix of two things:
it looks for items that are similar to what the user liked before (using text similarity), and it also considers how popular those items are. It then combines these two scores, ranks the items, and returns the top recommendations.
"""

def cold_start_user_recommend(user_reviews, top_n=10):
    # Case 1: completely new user
    if len(user_reviews) == 0:
        top_items = item_stats.sort_values(
            'popularity_score', ascending=False
        ).head(top_n)
        return top_items.index.tolist()

    # Case 2: user has some reviews → hybrid approach
    user_profile = tfidf.transform(user_reviews)
    sim_scores = linear_kernel(user_profile, tfidf_matrix).flatten()
    sim_scores = sim_scores / sim_scores.max()
    results = []
    for idx, item in enumerate(item_df['item_id']):
        content_sim = sim_scores[idx]
        pop_score = item_stats.loc[item, 'popularity_score'] \
            if item in item_stats.index else 0
        final_score = (
            0.6 * content_sim +
            0.4 * pop_score
        )

        results.append((item, final_score))
    results.sort(key=lambda x: x[1], reverse=True)
    return [item for item, _ in results[:top_n]]

"""We define a function to calculate Precision at K for evaluating our recommendation model:

1. We sort predictions by the estimated rating (pred.est) in descending order.

2. We select the top-K predicted items.

3. An item is considered relevant if its true rating (pred.r_ui) is 4 or higher.

4. Precision@K is the fraction of relevant items in the top-K recommendations.

This metric helps us measure how accurate the model’s top recommendations are for users.
"""

