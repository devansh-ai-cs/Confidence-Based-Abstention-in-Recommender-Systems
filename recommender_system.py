"""We define a function to recommend top-N items for a given user using our trained SVD model:

1. We get all unique items from the dataset.

2. For each item, we predict the rating the user would give using the trained SVD model.

3. We sort the items by predicted rating in descending order.

4. Finally, we return the top-N items as recommendations for that user.
"""

def recommend_cf(user_id,n=10):
    items = df['item_id'].unique()
    scores = []
    for item in items:
        pred = svd_model.predict(user_id,item)
        scores.append((item,pred.est))
    scores.sort(key=lambda x:x[1],reverse=True)
    return scores[:n]

"""We process the review text to use in a content-based recommendation model:

1. We fill missing review texts with empty strings to avoid errors.

2. TfidfVectorizer converts text into numerical features based on term frequency–inverse document frequency (TF-IDF).

3. Parameters:

    stop_words='english': remove common English words

    max_features=5000: keep only the top 5000 words

    min_df=5, max_df=0.8: include words that appear in at least 5 reviews but in no more than 80% of all reviews

4. tfidf_matrix stores the numerical representation of all reviews, ready for content-based analysis.
"""

item_df = df.groupby('item_id')['review'].apply(lambda x: ' '.join(x)).reset_index()
tfidf = TfidfVectorizer(
    stop_words='english',
    max_features=5000,
    min_df=5,
    max_df=0.8
)

tfidf_matrix = tfidf.fit_transform(item_df['review'])
print(f"\nTF-IDF Matrix Shape: {tfidf_matrix.shape}")
print(f"item_df shape: {item_df.shape}")
print(f"Number of unique items: {len(item_df)}")

"""We define a function to compute a content similarity score for a given item:

1. We use linear_kernel to calculate cosine similarity between the given item and all other items based on TF-IDF features.

2. The function computes how similar the item is to others in terms of review content.

3. We return the average similarity score, which can be used to rank items for content-based recommendations.
"""

def content_score(item_index):
    sim_scores = linear_kernel(
        tfidf_matrix[item_index:item_index+1],
        tfidf_matrix
    ).flatten()
    return sim_scores

"""e define a function to estimate the confidence of a predicted rating:

1. Ratings closer to 1 or 5 are considered more confident.

2. Ratings near 3 are less confident.

3. The function returns a normalized confidence score between 0 and 1.
"""

def prediction_confidence(pred_rating):
    confidence = abs(pred_rating - 3) / 2
    return confidence

"""We define a function to recommend top-N items by combining collaborative filtering and content-based methods:

1. For each item, we compute: collaborative filtering prediction, content similarity, and prediction confidence.

2. We combine them into a hybrid score using weighted averages (0.6 CF, 0.3 content, 0.1 confidence).

3. Items are sorted by this score, and we return the top-N recommendations for the user.
"""

def hybrid_recommend(user_id, top_n=10):
    items = item_df['item_id'].values
    results = []
    for item in items:
        cf_pred = svd_model.predict(user_id, item).est
        idx = item_df[item_df['item_id'] == item].index[0]
        content_sim = content_score(idx).mean()
        confidence = prediction_confidence(cf_pred)
        hybrid_score = (
            0.6 * cf_pred +
            0.3 * content_sim +
            0.1 * confidence
        )
        results.append((item, hybrid_score, confidence))
    results.sort(key=lambda x: x[1], reverse=True)
    return results[:top_n]

"""We define a function to recommend items only when the model is confident:

1. We first generate top-20 hybrid recommendations for the user.

2. For each item, we check the confidence score.

3. Only items with confidence above the threshold are included.

4. Finally, we return the top-10 confident recommendations.

This ensures that we avoid recommending items when the model is uncertain, improving reliability.
"""

def recommend_with_abstention(user_id, threshold=0.4):
    recs = hybrid_recommend(user_id, top_n=20)
    final_recs = []
    for item,score,conf in recs:
        if conf >= threshold:
            final_recs.append((item,score))
    return final_recs[:10]

"""We define a function to recommend items similar to a given item based on review content:

1. We calculate cosine similarity between the selected item and all other items using their TF-IDF features.

2. We sort items by similarity and select the top-N most similar items.

3. The function returns the item_id and review text of the recommended items.
"""

def recommend_content(item_id, top_n=10):
    idx = item_df[item_df['item_id'] == item_id].index[0]
    cosine_sim = linear_kernel(
        tfidf_matrix[idx:idx+1],
        tfidf_matrix
    ).flatten()
    similar_indices = cosine_sim.argsort()[-top_n-1:-1][::-1]
    return item_df.iloc[similar_indices]['item_id']

