"""We define a function to calculate Recall at K for evaluating our recommendation model:

1. We sort predictions by estimated rating (pred.est) in descending order.

2. We select the top-K predicted items.

3. An item is relevant if its true rating (pred.r_ui) is 4 or higher.

4. Recall@K is the fraction of all relevant items that appear in the top-K recommendations.

This metric helps us measure how well the model captures all relevant items in its top recommendations.
"""

def recall_at_k(predictions, k=10, threshold=4):
    user_preds = get_user_predictions(predictions)
    recalls = []
    for uid, user_ratings in user_preds.items():
        user_ratings.sort(key=lambda x: x.est, reverse=True)
        top_k = user_ratings[:k]
        relevant = sum((pred.r_ui >= threshold) for pred in top_k)
        total_relevant = sum((pred.r_ui >= threshold) for pred in user_ratings)
        if total_relevant > 0:
            recalls.append(relevant / total_relevant)
    return np.mean(recalls)

"""We define a function to calculate Normalized Discounted Cumulative Gain (NDCG) at K, which evaluates the quality of ranked recommendations:

1. We sort predictions by estimated rating (pred.est) in descending order.

2. DCG (Discounted Cumulative Gain) sums the relevance of top-K items, giving higher-ranked items more weight.

3. IDCG is the ideal DCG if items were perfectly ranked by true ratings.

4. NDCG@K is the ratio of DCG to IDCG, giving a score between 0 and 1.

This metric helps us assess how well the ranking of our recommendations aligns with actual user preferences.
"""

def ndcg_at_k(predictions, k=10):
    user_preds = get_user_predictions(predictions)
    ndcgs = []
    for uid, user_ratings in user_preds.items():
        user_ratings.sort(key=lambda x: x.est, reverse=True)
        dcg = 0
        for i, pred in enumerate(user_ratings[:k]):
            rel = pred.r_ui
            dcg += (2**rel - 1) / np.log2(i + 2)
        ideal = sorted([pred.r_ui for pred in user_ratings], reverse=True)[:k]
        idcg = 0
        for i, rel in enumerate(ideal):
            idcg += (2**rel - 1) / np.log2(i + 2)
        if idcg > 0:
            ndcgs.append(dcg / idcg)
    return np.mean(ndcgs)

"""We define a function to calculate MAP at K, which evaluates the quality of ranked recommendations:

1. We sort predictions by estimated rating (pred.est) in descending order.

2. For the top-K predicted items, we track “hits” where the true rating (pred.r_ui) is 4 or higher.

3. Each hit contributes precision at that rank to the score.

4. MAP@K is the average precision across all hits, normalized so it is between 0 and 1.

This metric helps us measure how precise the model is at ranking relevant items at the top of the recommendation list.
"""

def map_at_k(predictions, k=10, threshold=4):
    user_preds = get_user_predictions(predictions)
    maps = []
    for uid, user_ratings in user_preds.items():
        user_ratings.sort(key=lambda x: x.est, reverse=True)
        score = 0
        hits = 0
        for i, pred in enumerate(user_ratings[:k]):
            if pred.r_ui >= threshold:
                hits += 1
                score += hits / (i + 1)
        if hits > 0:
            maps.append(score / hits)
    return np.mean(maps)

"""The get_user_predictions function organizes predictions by user ID using a defaultdict, grouping all predictions for each user together."""

def get_user_predictions(predictions):
    """Helper function to organize predictions by user ID"""
    user_preds = defaultdict(list)
    for pred in predictions:
        user_preds[pred.uid].append(pred)
    return user_preds

